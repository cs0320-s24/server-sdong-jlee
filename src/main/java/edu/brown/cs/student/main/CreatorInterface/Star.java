package edu.brown.cs.student.main.CreatorInterface;

public class Star {

  public int id;
  public String ProperName;
  public double X;
  public double Y;
  public double Z;

  public Star(int id, String ProperName, double X, double Y, double Z) {
    this.id = id;
    this.ProperName = ProperName;
    this.X = X;
    this.Y = Y;
    this.Z = Z;
  }
}
